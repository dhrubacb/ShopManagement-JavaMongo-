 public void initialize(URL location, ResourceBundle resources) {
        ProductController con = new ProductController();
        con.connect();
        MongoCollection<Document> col = con.getData();
        
        for(Document doc : col.find())
        {
            int index = (int)doc.get("itemId");
            
            String name = (String) doc.get("Item Name");
            String _for = (String) doc.get("Price");
            String quan = (String) doc.get("Quantity");
              DefaultTableModel model;
            model = (DefaultTableModel) jTable1.getModel();
         model.addRow(new Object[]{index,name,_for,quan});
            
        }
        
        
    }
